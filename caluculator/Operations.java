package caluculator;

public class Operations {
	public static float add(float n1,float n2)
	{
		return n1+n2;
	}
	public static float sub(float n1,float n2)
	{
		return n1-n2;
	}
	public static float mul(float n1,float n2)
	{
		return n1*n2;
	}
	public static float div(float n1,float n2)
	{
		return n1/n2;
	}
}
